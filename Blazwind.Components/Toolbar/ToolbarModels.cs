namespace Blazwind.Components.Toolbar;

public enum ToolbarPosition
{
    None, // Normal akış içinde
    Top, // Üst kenar
    Bottom, // Alt kenar
    Left, // Sol kenar
    Right, // Sağ kenar
    TopLeft, // Sol üst köşe
    TopRight, // Sağ üst köşe
    BottomLeft, // Sol alt köşe
    BottomRight // Sağ alt köşe
}

public enum ToolbarOrientation
{
    Horizontal,
    Vertical
}